"""
Frame-level DiT with conditional flow matching (CFM) for multi-frame RGB video prediction.

This module predicts sequences of future RGB frames using a Diffusion Transformer (DiT)
conditioned on:
  - World action query tokens (learnable, one per frame)
  - Text embeddings from a frozen encoder (e.g., CLIP, T5)
  - Diffusion time t ∈ [0, 1]

The key innovation is that **world action tokens interact with text embeddings** through
cross-attention to generate frame-specific conditional embeddings. This allows semantic
text guidance to shape the spatiotemporal dynamics for each predicted frame.

The DiT learns a **spatial velocity field** v_θ(x_t, t, c_action) on 2D feature maps,
where x_t is the noisy latent at diffusion time t, and c_action is the conditional
embedding produced by cross-attention between world action tokens and text embeddings.

Training objective (same OT path as conditional flow matching)::

    x_0 ~ N(0, I),  t ~ U(0, 1),  x_1 = target RGB frame
    x_t = (1 - t) x_0 + t * x_1
    v*  = x_1 - x_0
    
    c_action[i] = CrossAttn(world_action_tokens[i] → text_embeddings)
    L_cfm = || v_θ(x_t, t, c_action[i]) - v* ||²

At inference, samples are generated by Euler ODE integration from noise.

Key features:
  * **Multi-frame prediction:** Configurable number of future frames to predict.
  * **Text-guided action tokens:** Learnable world action tokens attend to text via
    cross-attention, enabling rich semantic and temporal conditioning.
  * **Per-frame differentiation:** Each frame gets unique conditioning from its action token,
    allowing distinct temporal dynamics across the sequence.
  * **Efficient interaction:** Uses cross-attention rather than expensive per-token fusion,
    maintaining efficiency while enabling rich text-action interplay.
"""

from __future__ import annotations

from types import SimpleNamespace
from typing import Any, Dict, Optional, Tuple

import torch
import torch.nn as nn
import torch.nn.functional as F

from models.DiT import DiT


def make_dit_config(
    base_config: Any,
    in_channels: int,
    out_channels: Optional[int] = None,
) -> SimpleNamespace:
    """
    Build a namespace with configuration attributes for DiT initialization.
    
    This helper encapsulates common config extraction logic, defaulting to sensible
    values if attributes are missing from the base config.
    
    Args:
        base_config: Base config object (e.g., from training setup).
        in_channels: Number of input channels for the DiT (e.g., 3 for RGB).
        out_channels: Number of output channels. Defaults to in_channels if None.
    
    Returns:
        SimpleNamespace with DiT config attributes.
    """
    if out_channels is None:
        out_channels = in_channels
    return SimpleNamespace(
        dit_in_channels=in_channels,
        dit_out_channels=out_channels,
        dit_patch_size=getattr(base_config, "dit_patch_size", getattr(base_config, "patch_size", 4)),
        dit_hidden_size=getattr(base_config, "dit_hidden_size", getattr(base_config, "emb_dim", 768)),
        dit_depth=getattr(base_config, "dit_depth", 12),
        dit_num_heads=getattr(base_config, "dit_num_heads", 12),
        dit_mlp_ratio=getattr(base_config, "dit_mlp_ratio", 4.0),
        dit_max_resolution=getattr(base_config, "dit_max_resolution", 256),
        emb_dim=base_config.emb_dim,
        dit_time_freq_dim=getattr(base_config, "dit_time_freq_dim", 256),
    )


def flow_matching_velocity_loss(
    dit: DiT,
    x1: torch.Tensor,
    context: torch.Tensor,
) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    """
    Conditional flow matching loss for a single frame prediction.
    
    Samples a random time point t ∈ [0, 1] and constructs the OT path:
      x_t = (1 - t) * x_0 + t * x_1
    where x_0 is Gaussian noise and x_1 is the target RGB frame.
    
    The DiT predicts the velocity v = dx/dt ≈ x_1 - x_0, and we minimize
    the MSE between predicted and actual velocity.
    
    Args:
        dit: DiT model for velocity prediction.
        x1: [B, C, H, W] target frame (x_1 in the OT path).
        context: [B, emb_dim] conditional embedding from text-action cross-attention.
    
    Returns:
        Tuple of:
          - loss: scalar MSE loss
          - x_t: [B, C, H, W] noisy sample at time t (useful for auxiliary losses)
          - t: [B] time values
          - v_pred: [B, C, H, W] predicted velocity
    """
    b = x1.size(0)
    device = x1.device
    
    # Sample random time steps from Uniform[0, 1]
    t = torch.rand(b, device=device)
    
    # Sample Gaussian noise as x_0
    x0 = torch.randn_like(x1)
    
    # Construct noisy interpolation: x_t = (1-t) * x_0 + t * x_1
    te = t.view(b, 1, 1, 1)
    x_t = (1.0 - te) * x0 + te * x1
    
    # Target velocity (ground truth): v* = x_1 - x_0
    v_star = x1 - x0
    
    # Predict velocity via DiT
    v_pred = dit(x_t, t, context)
    
    # Compute MSE loss
    loss = F.mse_loss(v_pred, v_star)
    
    return loss, x_t, t, v_pred


@torch.no_grad()
def sample_di_t_euler(
    dit: DiT,
    context: torch.Tensor,
    channels: int,
    height: int,
    width: int,
    num_steps: int = 20,
) -> torch.Tensor:
    """
    Generate an RGB frame by integrating the DiT velocity field using Euler's method.
    
    Starts from Gaussian noise at t=0 and integrates forward to t=1 using fixed-step
    Euler scheme: x_{i+1} = x_i + v(x_i, t_i) * dt.
    
    Args:
        dit: DiT model for velocity prediction.
        context: [B, emb_dim] conditional embedding from text-action cross-attention.
        channels: Number of output channels (e.g., 3 for RGB).
        height: Height of the output frame in latent space.
        width: Width of the output frame in latent space.
        num_steps: Number of integration steps (higher = more accurate but slower).
    
    Returns:
        [B, channels, height, width] generated frame in latent space.
    """
    b = context.size(0)
    device = context.device
    
    # Initialize with Gaussian noise at t=0
    x = torch.randn(b, channels, height, width, device=device)
    
    # Compute time step size
    dt = 1.0 / num_steps
    
    # Euler integration from t=0 to t=1
    for i in range(num_steps):
        t_val = i * dt
        t = torch.full((b,), t_val, device=device)
        
        # Predict velocity at current step
        v = dit(x, t, context)
        
        # Euler update step
        x = x + v * dt
    
    return x
class RGBFramePredictor(nn.Module):
    """
    Multi-frame RGB predictor using DiT with conditional flow matching.
    
    This module predicts a sequence of future RGB frames using a single DiT conditioned on:
      1. World action query tokens (learnable, one per frame)
      2. Text embeddings (from frozen encoder, interacted via cross-attention)
      3. Diffusion time t ∈ [0, 1]
    
    The key innovation is that world action tokens **interact with text embeddings** through
    cross-attention to generate frame-specific conditional embeddings. This allows the text
    to guide the action tokens, creating rich spatiotemporal dynamics for each predicted frame.
    
    Key design choices:
      - Single DiT head for RGB (simplified from three heads for RGB/depth/flow).
      - World action tokens: learnable [num_frames, context_dim] embeddings that attend
        to text embeddings via cross-attention (not simple concatenation).
      - Configurable number of frames to predict (via num_predict_frames parameter).
      - Uses flow matching (OT path) loss during training.
    
    Architecture flow:
      world_action_tokens[i] --[cross-attention]--> text_embeddings --> conditional_emb[i]
                                                                                |
                                                                                v
                                                                          DiT(x_t, t, cond_emb[i])
    
    Usage:
        During training: call compute_loss(future_rgb, text_emb, num_frames=4)
        During inference: call predict_future_frames(height, width, text_emb, num_frames=4)
    """

    def __init__(self, config: Any, num_predict_frames: int = 4):
        """
        Initialize the RGB frame predictor with world action query tokens.
        
        Args:
            config: Configuration object with DiT hyperparameters
                   (dit_hidden_size, dit_depth, dit_num_heads, dit_patch_size, emb_dim, etc.).
            num_predict_frames: Maximum number of frames that can be predicted.
                               Each predicted frame gets its own world action token.
                               Default: 4 frames.
        """
        super().__init__()
        self.config = config
        self.num_predict_frames = num_predict_frames
        
        # RGB channels (typically 3 for RGB)
        rgb_ch = getattr(config, "rgb_channels", getattr(config, "dit_rgb_in_channels", 3))
        context_dim = config.emb_dim
        
        # Create the single DiT for RGB prediction
        self.dit_rgb = DiT(make_dit_config(config, rgb_ch, rgb_ch))
        
        # ==================== World Action Tokens ====================
        # Learnable query tokens: one per predicted frame
        # These tokens will attend to text embeddings to create frame-specific conditioning
        # Shape: [num_predict_frames, context_dim]
        self.world_action_tokens = nn.Parameter(
            torch.empty(num_predict_frames, context_dim)
        )
        nn.init.normal_(self.world_action_tokens, std=0.02)
        
        # ==================== Text-Action Cross-Attention ====================
        # Cross-attention: world_action_tokens (queries) attend to text_embeddings (keys/values)
        # This produces frame-specific conditional embeddings that combine text and action info
        num_heads = getattr(config, "dit_num_heads", 12)
        attn_drop = getattr(config, "dit_attn_drop", 0.0)
        self.text_action_cross_attn = nn.MultiheadAttention(
            embed_dim=context_dim,
            num_heads=num_heads,
            batch_first=True,
            dropout=attn_drop,
        )

        # Pre-FFN norm (after cross-attn residual)
        self.cross_attn_norm = nn.LayerNorm(context_dim)

        # Feed-forward block — 2× expansion keeps conditioning lightweight
        self.cross_attn_proj = nn.Sequential(
            nn.Linear(context_dim, context_dim * 2),
            nn.GELU(),
            nn.Linear(context_dim * 2, context_dim),
        )

        # Post-FFN norm (residual + norm, mirrors standard transformer block)
        self.proj_norm = nn.LayerNorm(context_dim)

        # Projection for world_video_query_hiddens from HaloVLM transformer.
        # When provided these replace the static world_action_tokens as cross-attn queries.
        wv_in = getattr(config, "emb_dim", context_dim)
        self.wv_query_proj = (
            nn.Linear(wv_in, context_dim) if wv_in != context_dim else nn.Identity()
        )
    
    def _create_augmented_context(
        self,
        text_emb: torch.Tensor,
        num_frames: int,
        frame_idx: Optional[int] = None,
        world_video_query: Optional[torch.Tensor] = None,
    ) -> torch.Tensor:
        """
        Create frame-specific conditional embeddings via world action token-text interaction.
        
        This method uses cross-attention to fuse world action query tokens with text embeddings.
        Each frame's action token attends to the full text sequence, creating a rich conditional
        embedding that combines temporal action information with semantic text guidance.
        
        Architecture:
          - Queries (Q): world_action_tokens[frame_idx] for the specific frame
          - Keys (K), Values (V): text_embeddings (frozen encoder output)
          - Output: conditional embedding [B, 1, emb_dim] refined via feed-forward
        
        Args:
            text_emb: [B, seq_len, emb_dim] or [B, emb_dim] text embeddings from frozen encoder.
                     If 2D, will be expanded to [B, 1, emb_dim] as a single summary token.
            num_frames: Total number of frames being predicted.
            frame_idx: Optional frame index (0 to num_frames-1). If None, uses average
                      of all action tokens for this batch of frames.
        
        Returns:
            conditional_emb: [B, emb_dim] conditional embedding for DiT from text-action interaction.
        
        Process:
          1. Get frame-specific action token(s)
          2. Expand to batch: [1, emb_dim] → [B, 1, emb_dim]
          3. Apply cross-attention: action_token attends to text_emb
          4. Refine with feed-forward projection
          5. Return [B, emb_dim] for DiT conditioning
        """
        batch_size = text_emb.size(0)
        device = text_emb.device

        # Handle text embedding shape: convert to [B, seq_len, emb_dim]
        if text_emb.dim() == 2:
            # [B, emb_dim] → [B, 1, emb_dim] (treat as single token)
            text_emb_seq = text_emb.unsqueeze(1)
        else:
            # Already [B, seq_len, emb_dim]
            text_emb_seq = text_emb
        
        if world_video_query is not None:
            # world_video_query: [B, emb_dim] — contextualized hidden state from HaloVLM.
            # Project to context_dim (no-op when dims match) and use as cross-attn query.
            action_query = self.wv_query_proj(world_video_query).unsqueeze(1)  # [B, 1, context_dim]
        else:
            # Fall back to learnable world_action_tokens (static per-frame embeddings).
            action_tokens = self.world_action_tokens[:num_frames]  # [num_frames, emb_dim]
            if frame_idx is not None:
                action_token = action_tokens[frame_idx]  # [emb_dim]
            else:
                action_token = action_tokens.mean(dim=0)  # [emb_dim]
            action_query = action_token.unsqueeze(0).unsqueeze(0).expand(batch_size, -1, -1)
            # [B, 1, context_dim]
        
        # ==================== Cross-Attention: Action Token → Text ====================
        # Q: action_query [B, 1, emb_dim] — what to predict
        # K, V: text_emb_seq [B, seq_len, emb_dim] — text guidance
        # Output: [B, 1, emb_dim] — attended action token refined by text
        attn_output, _ = self.text_action_cross_attn(
            query=action_query,
            key=text_emb_seq,
            value=text_emb_seq,
            need_weights=False,
        )
        # attn_output: [B, 1, emb_dim]
        
        # Residual + norm after cross-attention (pre-FFN norm)
        action_query_refined = self.cross_attn_norm(attn_output.squeeze(1) + action_query.squeeze(1))
        # [B, emb_dim]

        # FFN with residual + post-FFN norm (standard transformer block structure)
        conditional_emb = self.proj_norm(action_query_refined + self.cross_attn_proj(action_query_refined))
        # [B, emb_dim]

        return conditional_emb

    def _create_all_frame_contexts(
        self,
        text_emb: torch.Tensor,
        num_frames: int,
        world_video_query_hiddens: Optional[torch.Tensor] = None,
    ) -> torch.Tensor:
        """
        Batched version of _create_augmented_context: processes all frames in one
        cross-attention + FFN call instead of a Python loop.

        Args:
            text_emb: [B, S, emb_dim] or [B, emb_dim]
            num_frames: number of frames to condition
            world_video_query_hiddens: [B, n_wv, emb_dim] or None

        Returns:
            [B * num_frames, emb_dim] — one conditioning vector per (batch, frame) pair,
            in row-major order (all frames of sample 0, then all of sample 1, …).
        """
        B = text_emb.size(0)
        text_seq = text_emb.unsqueeze(1) if text_emb.dim() == 2 else text_emb  # [B, S, D]
        S = text_seq.size(1)

        # Build action queries [B, num_frames, emb_dim]
        if world_video_query_hiddens is not None:
            n_wv = min(num_frames, world_video_query_hiddens.size(1))
            action_query = self.wv_query_proj(world_video_query_hiddens[:, :n_wv])  # [B, n_wv, D]
            if n_wv < num_frames:
                pad = self.world_action_tokens[n_wv:num_frames].unsqueeze(0).expand(B, -1, -1)
                action_query = torch.cat([action_query, pad], dim=1)
        else:
            action_query = self.world_action_tokens[:num_frames].unsqueeze(0).expand(B, -1, -1)
        # [B, num_frames, emb_dim]

        # Flatten to [B*F, 1, D] and [B*F, S, D] for a single batched cross-attn call
        BF = B * num_frames
        aq_flat = action_query.reshape(BF, 1, -1)
        text_flat = text_seq.unsqueeze(1).expand(-1, num_frames, -1, -1).reshape(BF, S, -1)

        attn_out, _ = self.text_action_cross_attn(
            aq_flat, text_flat, text_flat, need_weights=False,
        )  # [BF, 1, D]

        refined = self.cross_attn_norm(attn_out.squeeze(1) + aq_flat.squeeze(1))  # [BF, D]
        cond = self.proj_norm(refined + self.cross_attn_proj(refined))             # [BF, D]
        return cond

    def compute_loss(
        self,
        future_rgb: torch.Tensor,
        text_emb: torch.Tensor,
        num_frames: int = 4,
        world_video_query_hiddens: Optional[torch.Tensor] = None,
    ) -> Dict[str, torch.Tensor]:
        """
        Compute the training loss for multi-frame RGB prediction with text-guided action.

        For each frame in the sequence, we compute a conditional flow matching loss,
        using frame-specific world action tokens interacting with text embeddings via
        cross-attention to guide the prediction.

        Args:
            future_rgb: [B, num_frames, 3, H, W] stack of target frames to predict.
                       For each frame in the sequence, we compute a separate CFM loss.
            text_emb: [B, seq_len, emb_dim] or [B, emb_dim] text embeddings from frozen encoder.
                     These guide the world action tokens to create frame-specific conditioning.
            num_frames: Number of frames being predicted. Should match future_rgb.size(1).
                       Used to determine world action token selection.
        
        Returns:
            Dictionary with:
              - "loss_rgb_cfm": [1,] total CFM loss averaged over all frames
              - "loss_rgb_total": [1,] alias for loss_rgb_cfm (for compatibility)
        
        Flow:
          For each frame i in [0, num_frames):
            1. Get world_action_tokens[i]
            2. Cross-attend with text_emb: action_token --[attn]--> text → conditional_emb[i]
            3. Compute CFM loss: L_i = ||v_θ(x_t, t, conditional_emb[i]) - v*||²
            4. Sum losses and average
        """
        out: Dict[str, torch.Tensor] = {}

        if future_rgb.dim() == 4:
            future_rgb = future_rgb.unsqueeze(1)  # [B, 3, H, W] → [B, 1, 3, H, W]

        # Clamp num_frames to what is actually available
        num_frames = min(num_frames, future_rgb.size(1), self.num_predict_frames)
        future_rgb = future_rgb[:, :num_frames]

        B, F, C, H, W = future_rgb.shape

        # Build all conditioning vectors in one batched call → [B*F, emb_dim]
        cond_all = self._create_all_frame_contexts(text_emb, F, world_video_query_hiddens)

        # Flatten frames into the batch dimension for a single DiT forward pass
        targets_flat = future_rgb.reshape(B * F, C, H, W)

        loss, _, _, _ = flow_matching_velocity_loss(self.dit_rgb, targets_flat, cond_all)

        out["loss_rgb_cfm"] = loss
        out["loss_rgb_total"] = loss
        return out
    
    @torch.no_grad()
    def predict_future_frames(
        self,
        height: int,
        width: int,
        text_emb: torch.Tensor,
        num_frames: int = 4,
        num_steps: int = 20,
        world_video_query_hiddens: Optional[torch.Tensor] = None,
    ) -> torch.Tensor:
        """
        Generate a sequence of future RGB frames with text-guided world action conditioning.

        Each frame is generated independently using its corresponding world action token,
        which is refined through cross-attention with text embeddings. This creates
        frame-specific conditioning that combines temporal action dynamics with text guidance.

        Args:
            height: Height of the output frames in latent space.
            width: Width of the output frames in latent space.
            text_emb: [B, seq_len, emb_dim] or [B, emb_dim] text embeddings from frozen encoder.
                     These guide world action tokens to create frame-specific conditioning.
            num_frames: Number of frames to generate. Must be <= num_predict_frames.
            num_steps: Number of ODE integration steps (higher = slower but more accurate).
        
        Returns:
            [B, num_frames, 3, height, width] generated RGB frames in latent space.
        
        Process per frame:
          1. Select world_action_tokens[frame_idx]
          2. Cross-attend with text_emb to get frame-specific conditioning
          3. Euler integrate DiT velocity field from t=0 to t=1
          4. Stack all frames into output tensor
        """
        num_frames = min(num_frames, self.num_predict_frames)

        B = text_emb.size(0)
        rgb_channels = getattr(self.config, "rgb_channels", 3)

        # Build all conditioning vectors in one batched call → [B*F, emb_dim]
        cond_all = self._create_all_frame_contexts(text_emb, num_frames, world_video_query_hiddens)

        # Generate all frames with a single Euler integration pass
        # sample_di_t_euler handles arbitrary batch sizes, so [B*F] works directly
        generated_flat = sample_di_t_euler(
            self.dit_rgb, cond_all, rgb_channels, height, width, num_steps=num_steps,
        )  # [B*F, C, H, W]

        return generated_flat.view(B, num_frames, rgb_channels, height, width)


