"""
Frame-level DiT with conditional flow matching (CFM) for video prediction tasks.

This module sits next to the existing action flow-matching head in ``HaloVLM``:
the *robot action* decoder learns a velocity field in a flat vector space, while
here the DiT learns a **spatial** velocity field v_θ(x_t, t, c) on 2D feature maps
(RGB frames, scalar depth, or 2-channel optical flow).

Training objective (same OT path as ``compute_flow_loss`` in ``halo_vla.py``)::

    x_0 ~ N(0, I),  t ~ U(0, 1),  x_1 = target map (RGB / depth / flow)
    x_t = (1 - t) x_0 + t x_1
    v*  = x_1 - x_0
    L_cfm = || v_θ(x_t, t, c) - v* ||²

At inference, samples are generated by Euler integration from noise, identical in
spirit to ``sample_actions``.

Additional losses (as requested):

* **Future RGB / depth:** same CFM loss; depth targets are a fixed differentiable
  pseudo-depth map from luminance (a stand-in when no LiDAR is available).

* **Depth self-supervision (reconstruction):** a shallow ``DepthToRgbDecoder`` is
  trained so that maps that approximate depth (including the CFM endpoint estimate
  ``x̂_1 = x_t + (1-t) v_θ``) explain the future RGB under an L1 reconstruction term.

* **Optical flow:** CFM with a **pseudo target** from one-step brightness
  constancy (gradient-based displacement). Plus photometric loss using the same
  endpoint trick, and a Charbonnier smoothness prior on the flow field.
"""

from __future__ import annotations

from types import SimpleNamespace
from typing import Any, Dict, Optional, Tuple

import torch
import torch.nn as nn
import torch.nn.functional as F

from models.DiT import DiT


def make_dit_config(
    base_config: Any,
    in_channels: int,
    out_channels: Optional[int] = None,
) -> SimpleNamespace:
    """Build a namespace with every attribute ``DiT.__init__`` reads from config."""
    if out_channels is None:
        out_channels = in_channels
    return SimpleNamespace(
        dit_in_channels=in_channels,
        dit_out_channels=out_channels,
        dit_patch_size=base_config.dit_patch_size,
        dit_hidden_size=base_config.dit_hidden_size,
        dit_depth=base_config.dit_depth,
        dit_num_heads=base_config.dit_num_heads,
        dit_mlp_ratio=base_config.dit_mlp_ratio,
        dit_max_resolution=base_config.dit_max_resolution,
        emb_dim=base_config.emb_dim,
        dit_time_freq_dim=getattr(base_config, "dit_time_freq_dim", 256),
    )


class FrameContextEncoder(nn.Module):
    """
    Encode one or more past RGB frames into a single conditioning vector c ∈ ℝ^{emb_dim}.

    The DiT uses the same conditioning interface as the rest of Halo-VLA (``emb_dim``).
    """

    def __init__(self, emb_dim: int = 512, in_ch: int = 3):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv2d(in_ch, 64, kernel_size=7, stride=2, padding=3),
            nn.ReLU(inplace=True),
            nn.Conv2d(64, 128, kernel_size=3, stride=2, padding=1),
            nn.ReLU(inplace=True),
            nn.Conv2d(128, 256, kernel_size=3, stride=2, padding=1),
            nn.ReLU(inplace=True),
            nn.Conv2d(256, 256, kernel_size=3, stride=2, padding=1),
            nn.ReLU(inplace=True),
            nn.AdaptiveAvgPool2d(1),
        )
        self.fc = nn.Linear(256, emb_dim)

    def forward(self, frames: torch.Tensor) -> torch.Tensor:
        """
        frames: [B, T, 3, H, W] or [B, 3, H, W]
        returns: [B, emb_dim]
        """
        if frames.dim() == 5:
            b, t, c, h, w = frames.shape
            x = frames.reshape(b * t, c, h, w)
            feat = self.net(x).flatten(1)
            feat = feat.view(b, t, -1).mean(dim=1)
            return self.fc(feat)
        return self.fc(self.net(frames).flatten(1))


class DepthToRgbDecoder(nn.Module):
    """Maps a single-channel depth-like map to RGB for reconstruction loss."""

    def __init__(self, hidden: int = 64):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv2d(1, hidden, 3, padding=1),
            nn.ReLU(inplace=True),
            nn.Conv2d(hidden, hidden, 3, padding=1),
            nn.ReLU(inplace=True),
            nn.Conv2d(hidden, 3, 3, padding=1),
        )

    def forward(self, depth: torch.Tensor) -> torch.Tensor:
        return self.net(depth)


def rgb_to_pseudo_depth(rgb: torch.Tensor, eps: float = 1e-3) -> torch.Tensor:
    """
    Cheap monocular proxy: inverse intensity, log-stabilised, min–max per image.

    rgb: [B, 3, H, W] (normalised ImageNet range is fine)
    returns: [B, 1, H, W]
    """
    gray = rgb.mean(dim=1, keepdim=True)
    d = 1.0 / (gray.abs() + eps)
    d = torch.log1p(d)
    d_min = d.amin(dim=(2, 3), keepdim=True)
    d_max = d.amax(dim=(2, 3), keepdim=True)
    return (d - d_min) / (d_max - d_min + eps)


def gradient_flow_pseudo(I0: torch.Tensor, I1: torch.Tensor, eps: float = 1e-4) -> torch.Tensor:
    """
    One-step brightness-constancy flow (u, v) from gradients of I0 and intensity change.

    I0, I1: [B, 3, H, W] — returns [B, 2, H, W] (dx, dy) in pixel units, fully differentiable.
    """
    g0 = I0.mean(dim=1, keepdim=True)
    g1 = I1.mean(dim=1, keepdim=True)
    Iz = g1 - g0
    gx = g0[:, :, :, 1:] - g0[:, :, :, :-1]
    gx = F.pad(gx, (0, 1, 0, 0), mode="replicate")
    gy = g0[:, :, 1:, :] - g0[:, :, :-1, :]
    gy = F.pad(gy, (0, 0, 0, 1), mode="replicate")
    denom = gx * gx + gy * gy + eps
    u = -Iz * gx / denom
    v = -Iz * gy / denom
    return torch.cat([u, v], dim=1)


def backward_warp(img: torch.Tensor, flow: torch.Tensor) -> torch.Tensor:
    """
    img: [B, C, H, W], flow: [B, 2, H, W] with (dx, dy) displacement.
    Samples img at (x + dx, y + dy) to align I0 toward I1 coordinates.
    """
    b, _, h, w = flow.shape
    y, x = torch.meshgrid(
        torch.linspace(-1, 1, h, device=img.device, dtype=img.dtype),
        torch.linspace(-1, 1, w, device=img.device, dtype=img.dtype),
        indexing="ij",
    )
    grid_x = x.unsqueeze(0).expand(b, -1, -1)
    grid_y = y.unsqueeze(0).expand(b, -1, -1)
    dx = flow[:, 0] / max(w - 1, 1) * 2
    dy = flow[:, 1] / max(h - 1, 1) * 2
    grid = torch.stack([grid_x + dx, grid_y + dy], dim=-1)
    return F.grid_sample(img, grid, mode="bilinear", padding_mode="border", align_corners=True)


def flow_smoothness_loss(flow: torch.Tensor) -> torch.Tensor:
    """Charbonnier penalty on spatial flow gradients."""
    dx = flow[:, :, :, 1:] - flow[:, :, :, :-1]
    dy = flow[:, :, 1:, :] - flow[:, :, :-1, :]
    c = 1e-3
    return (dx * dx + c * c).sqrt().mean() + (dy * dy + c * c).sqrt().mean()


def flow_matching_velocity_loss(
    dit: DiT,
    x1: torch.Tensor,
    context: torch.Tensor,
) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    """
    Conditional flow matching on a tensor field (one forward through DiT).

    Returns (loss, x_t, t, v_pred) for optional auxiliary terms.
    """
    b = x1.size(0)
    device = x1.device
    t = torch.rand(b, device=device)
    x0 = torch.randn_like(x1)
    te = t.view(b, 1, 1, 1)
    x_t = (1.0 - te) * x0 + te * x1
    v_star = x1 - x0
    v_pred = dit(x_t, t, context)
    loss = F.mse_loss(v_pred, v_star)
    return loss, x_t, t, v_pred


def cfm_endpoint_estimate(x_t: torch.Tensor, v_pred: torch.Tensor, t: torch.Tensor) -> torch.Tensor:
    """
    Along the OT line, x_1 = x_t + (1 - t) v with v ≈ x_1 - x_0 (exact if v is ideal).
    t: [B] broadcast to map shape.
    """
    te = t.view(-1, 1, 1, 1)
    return x_t + (1.0 - te) * v_pred


@torch.no_grad()
def sample_di_t_euler(
    dit: DiT,
    context: torch.Tensor,
    channels: int,
    height: int,
    width: int,
    num_steps: int = 20,
) -> torch.Tensor:
    """Generate a field by integrating the learned velocity from t=0 to t=1."""
    b = context.size(0)
    device = context.device
    x = torch.randn(b, channels, height, width, device=device)
    dt = 1.0 / num_steps
    for i in range(num_steps):
        t_val = i * dt
        t = torch.full((b,), t_val, device=device)
        v = dit(x, t, context)
        x = x + v * dt
    return x


class VisualDiTPredictor(nn.Module):
    """
    Three DiT heads (RGB future, depth future, optical flow) sharing one context encoder.

    Use ``compute_losses`` during training and ``predict_*`` helpers at inference.
    """

    def __init__(self, config: Any):
        super().__init__()
        self.config = config
        self.context_encoder = FrameContextEncoder(emb_dim=config.emb_dim, in_ch=3)
        self.dit_rgb = DiT(make_dit_config(config, config.dit_rgb_in_channels, config.dit_rgb_in_channels))
        self.dit_depth = DiT(make_dit_config(config, config.dit_depth_in_channels, config.dit_depth_in_channels))
        self.dit_flow = DiT(make_dit_config(config, config.dit_flow_in_channels, config.dit_flow_in_channels))
        self.depth_to_rgb = DepthToRgbDecoder()

    def encode_context(self, past_rgb: torch.Tensor) -> torch.Tensor:
        return self.context_encoder(past_rgb)

    def compute_losses(
        self,
        past_rgb: torch.Tensor,
        future_rgb: torch.Tensor,
        *,
        context_emb: Optional[torch.Tensor] = None,
        compute_rgb: bool = True,
        compute_depth: bool = True,
        compute_flow: bool = True,
    ) -> Dict[str, torch.Tensor]:
        """
        past_rgb:   [B, T, 3, H, W] or [B, 3, H, W] — conditioning frames
        future_rgb: [B, 3, H, W] — the frame to predict (x_1 for RGB / derived targets)
        context_emb: [B, emb_dim] — if set (e.g. ViT+pooled past from HaloVLM.forward),
                     used as DiT conditioning instead of the internal CNN encoder.

        Returns a dict of scalar losses suitable for backprop.
        """
        if context_emb is not None:
            ctx = context_emb
        else:
            ctx = self.encode_context(past_rgb)
        out: Dict[str, torch.Tensor] = {}
        cfg = self.config

        if compute_rgb:
            loss_rgb, _, _, _ = flow_matching_velocity_loss(self.dit_rgb, future_rgb, ctx)
            out["loss_rgb_cfm"] = loss_rgb

        if compute_depth:
            d1 = rgb_to_pseudo_depth(future_rgb).detach()
            loss_d, x_t, t, v_pred = flow_matching_velocity_loss(self.dit_depth, d1, ctx)
            out["loss_depth_cfm"] = loss_d
            d_hat = cfm_endpoint_estimate(x_t, v_pred, t)
            rgb_hat = self.depth_to_rgb(d_hat)
            out["loss_depth_recon"] = F.l1_loss(rgb_hat, future_rgb)

        if compute_flow:
            if past_rgb.dim() == 5:
                i0 = past_rgb[:, -1]
            else:
                i0 = past_rgb
            flow_star = gradient_flow_pseudo(i0, future_rgb).detach()
            loss_f, x_tf, tf, vf = flow_matching_velocity_loss(self.dit_flow, flow_star, ctx)
            out["loss_flow_cfm"] = loss_f
            flow_hat = cfm_endpoint_estimate(x_tf, vf, tf)
            i_w = backward_warp(i0, flow_hat)
            out["loss_flow_photo"] = F.l1_loss(i_w, future_rgb)
            out["loss_flow_smooth"] = flow_smoothness_loss(flow_hat)

        # Weighted total (caller may instead weight manually)
        total = torch.tensor(0.0, device=future_rgb.device)
        if compute_rgb:
            total = total + out["loss_rgb_cfm"]
        if compute_depth:
            total = (
                total
                + out["loss_depth_cfm"]
                + cfg.dit_depth_recon_weight * out["loss_depth_recon"]
            )
        if compute_flow:
            total = (
                total
                + out["loss_flow_cfm"]
                + cfg.dit_flow_photo_weight * out["loss_flow_photo"]
                + cfg.dit_flow_smooth_weight * out["loss_flow_smooth"]
            )
        out["loss_visual_total"] = total
        return out

    @torch.no_grad()
    def predict_future_rgb(
        self,
        past_rgb: torch.Tensor,
        height: int,
        width: int,
        context_emb: Optional[torch.Tensor] = None,
    ) -> torch.Tensor:
        ctx = context_emb if context_emb is not None else self.encode_context(past_rgb)
        return sample_di_t_euler(
            self.dit_rgb,
            ctx,
            self.config.dit_rgb_in_channels,
            height,
            width,
            num_steps=self.config.dit_num_sample_steps,
        )

    @torch.no_grad()
    def predict_future_depth(
        self,
        past_rgb: torch.Tensor,
        height: int,
        width: int,
        context_emb: Optional[torch.Tensor] = None,
    ) -> torch.Tensor:
        ctx = context_emb if context_emb is not None else self.encode_context(past_rgb)
        return sample_di_t_euler(
            self.dit_depth,
            ctx,
            self.config.dit_depth_in_channels,
            height,
            width,
            num_steps=self.config.dit_num_sample_steps,
        )

    @torch.no_grad()
    def predict_optical_flow(
        self,
        past_rgb: torch.Tensor,
        height: int,
        width: int,
        context_emb: Optional[torch.Tensor] = None,
    ) -> torch.Tensor:
        ctx = context_emb if context_emb is not None else self.encode_context(past_rgb)
        return sample_di_t_euler(
            self.dit_flow,
            ctx,
            self.config.dit_flow_in_channels,
            height,
            width,
            num_steps=self.config.dit_num_sample_steps,
        )
